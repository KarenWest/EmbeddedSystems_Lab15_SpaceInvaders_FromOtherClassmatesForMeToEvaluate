	void Switch_Init(void){ 
		volatile unsigned long delay;
		
		SYSCTL_RCGC2_R |= SYSCTL_RCGC2_GPIOE; // Activate clock for Port E
		delay = SYSCTL_RCGC2_R;
		GPIO_PORTE_AMSEL_R &= ~0x03;    // Clear the PE0 and PE1 bits in Port E AMSEL to disable analog
		GPIO_PORTE_PCTL_R &= ~0x000000FF;   // Clear the PE1 and PE0 bit fields in Port E PCTL to configure as GPIO
		GPIO_PORTE_DIR_R &= ~0x03;          // PE0-1 in
		GPIO_PORTE_AFSEL_R &= ~0x03;        // Clear the PE1 and PE0 bits in Port E AFSEL to disable alternate functions
		GPIO_PORTE_DEN_R |= 0x03;          // Set the PE1 and PE0 bits in Port E DEN to enable digital
	}
	
	
