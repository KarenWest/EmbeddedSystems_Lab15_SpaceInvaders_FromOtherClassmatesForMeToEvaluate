// Convert a 12-bit binary ADC sample into a 32-bit unsigned
// fixed-point pixel position.  
// Input: sample  12-bit ADC sample
// Output: 32-bit pixel
unsigned long Convert(unsigned long sample){
		unsigned long A = 16;
		unsigned long B = 0;

		return ((A*sample)>>10)+B; 
}


