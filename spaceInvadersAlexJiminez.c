	void PortF_init(){
		volatile unsigned long delay;
		// initialize profiling on PF1 (optional)
		SYSCTL_RCGC2_R |= 0x00000020; // activate port F
		delay = SYSCTL_RCGC2_R; //    wait for clock to stabilize
		GPIO_PORTF_LOCK_R = 0x4C4F434B;   // 2) unlock PortF PF0
		GPIO_PORTF_CR_R |= 0x0E;           // allow changes to PF1-3
		GPIO_PORTF_DIR_R |= 0x0E;   // make PF1 output
		GPIO_PORTF_AFSEL_R &= ~0x0E;// disable alt funct on PF1-3
		GPIO_PORTF_DEN_R |= 0x0E;   // enable digital I/O on PF1-3
		GPIO_PORTF_PCTL_R &= ~0x0000FFF0;
		GPIO_PORTF_AMSEL_R &= ~0x0E;     // disable analog functionality on PF1-3
	}



	// Initialize SysTick interrupts to trigger at 30 Hz, 33.33 ms
	void SysTick_Init(void){
		NVIC_ST_CTRL_R = 0;           // disable SysTick during setup
		NVIC_ST_RELOAD_R = 2666666;     // reload value for 30 Hz (assuming 80MHz)
		NVIC_ST_CURRENT_R = 0;        // any write to current clears it
		NVIC_SYS_PRI3_R = NVIC_SYS_PRI3_R&0x00FFFFFF; // priority 0               
		NVIC_ST_CTRL_R = 0x00000007;  // enable with core clock and interrupts
	}
	// executes every 33.33 ms, collects a sample, converts and stores in mailbox
	void SysTick_Handler(void){ 
		unsigned long ADCdata_temp;
		unsigned long In_temp;
	
		//We are playing
		if(play == 1){
			GPIO_PORTF_DATA_R ^= 0x02;
			GPIO_PORTF_DATA_R ^= 0x02;
			
			ADCdata_temp = ADC0_In();
			
			if(ADCdata != ADCdata_temp){
				ADCdata = ADCdata_temp;
				//Move player ship
				Move_Ship(Convert(ADCdata_temp));
			}

			In_temp = GPIO_PORTE_DATA_R&0x03;   // read PE0-1
			
			if(In_temp == 1 && In_temp != In){
				//fire a missile
				Create_missile(Convert(ADCdata_temp), find_index_missile(), 0, 1);
				Sound_Play(shoot, 4080);
			
			} else if((In_temp == 2 && In_temp != In)){
				//fire a laser
				Create_missile(Convert(ADCdata_temp), find_index_missile(), 0, 0);
				Sound_Play(shoot, 4080);
			}
			In = In_temp;
			Move_Missile();	

			m1 = ((Random_number()>>24)%number_invaders);
			n = ((Random_number()>>24)%shoot_probability)+1;
			
			if(Enemy[m1].life > 0 && Enemy[m1].enable == 1){
				if(n == 10){
					Create_missile(Enemy[m1].x, find_index_missile(), 1, (Random_number()>>24)%2);
					Sound_Play(shoot, 4080);
				}
			}
			Move_Enemy(game_speed);
			
			Collision();
			CheckExplosion();			
			CheckStatus();					

			GPIO_PORTF_DATA_R ^= 0x02;
			Semaphore = 1;
		}else{
			GPIO_PORTF_DATA_R = 0x02;
		}
		
	}


