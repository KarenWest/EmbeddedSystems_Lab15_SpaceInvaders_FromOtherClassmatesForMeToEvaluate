// generate a new pipe gap location
unsigned long NewPipeGap(void){
	unsigned long newgap;
	newgap = (Random() % (SCREENH - (PIPEGAPH + PIPEENDH) )) + PIPEGAPH + PIPEENDH-1;
	// make it even?
	newgap = newgap + (newgap % 2);
	return newgap;
}

