//********Convert****************
// Convert a 12-bit binary ADC sample into a 32-bit unsigned
// y coordinate ~ 9-47
unsigned long Convert(unsigned long sample){
  unsigned long x;

  sample &= 0x00000FFF;
  x = (( 10 * sample) >> 10) + BIRDH ;
  // check bounds
  if(x>SCREENH-1){
    x = SCREENH -1;
  }
  return x;
}

// detect collision
unsigned long DetectCollision(void){
	int i;
	// player hits ground
	if( Player.y >= SCREENH-1){
		return 1;
	}
	// player hits pipe
	for(i=0;i<TOTALPIPES;i++){
		// is pipe in range of bird?
		if(Walls[i].invisible == 0 && Walls[i].x < (Player.x + BIRDW) && Walls[i].x + PIPEENDW > Player.x){
			if(Player.y > Walls[i].y - PIPEENDH || Player.y - BIRDH < Walls[i].y - PIPEGAPH){
				// play sound
			  Sound_Explosion();
				// disable this wall
				Walls[i].invisible = 1;
				return 1;
			}
		}
	}
	// no collision!
	return 0;
}
