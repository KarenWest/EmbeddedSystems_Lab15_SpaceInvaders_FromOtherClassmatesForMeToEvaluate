// game data structure
struct GameState {
  unsigned short score;         // current score
  unsigned short highscore;     // session high score
  unsigned short frame;         // animation frame counter
};       
typedef struct GameState GSTyp;
GSTyp Game;

// player data structure
struct PlayerState {
  unsigned short x;      // x coordinate
  unsigned short y;      // y coordinate
  unsigned short targetY; // y coordinate player is aiming towards
  const unsigned char *image[PLAYERFRAMES]; // pointers to images
  unsigned short life;            // 0=dead, 1=alive
};       
typedef struct PlayerState PSTyp;
PSTyp Player;

// wall data structure
struct WallState {
  signed short x;      // x coordinate
  signed short originalx;      // original x coordinate
  unsigned short y;      // y coordinate of gap
  unsigned short invisible;      // is pipe invisible, no collisions
  const unsigned char *image[2]; // pointers to images
};       
typedef struct WallState WSTyp;
WSTyp Walls[TOTALPIPES];

// initial game state
void Init(void){
	// reset systick semaphore
	Flag = 0;
	// reset game
	Game.score = 0;
	Game.highscore = 0;
	Game.frame = 0;
}

// initial player state
void InitPlayer(void){
	int i=0;
  // player
	Player.x = PLAYERSTARTX;
	Player.y = PLAYERSTARTY;
	Player.targetY = PLAYERSTARTY;
	Player.life = PLAYERLIVES;
  Player.image[i++] = bird0;
  Player.image[i++] = bird0;
  Player.image[i++] = bird1;
  Player.image[i++] = bird1;
  Player.image[i++] = bird2;
  Player.image[i++] = bird2;
  Player.image[i++] = bird1;
  Player.image[i++] = bird1;
  // collision at frame 8
  Player.image[i++] = birdcrash1;
  Player.image[i++] = birdcrash1;
  Player.image[i++] = birdcrash1;
  Player.image[i++] = birdcrash1;
}

// initial walls state
void InitWalls(void){
  int i;
  // walls
  for(i=0;i<TOTALPIPES;i++){
    Walls[i].invisible = 0;
    Walls[i].x = (i) * ( PIPEENDW + PIPEGAPW ) + PIPESTARTX;
    // save original x coordinate for reload?
    Walls[i].originalx = Walls[i].x;
    // new gap location
    Walls[i].y = NewPipeGap();
    Walls[i].image[0] = pipe;
    // cap
    Walls[i].image[1] = pipeend;
  }
}

void Move(void){
	int i;
	Player.targetY = Convert(ADCdata);
	// aim player using slidepot
	Player.y = Player.targetY;
	// gravity ?
  // keep walls within bounds of display
  for(i=0;i<TOTALPIPES;i++){
    if(Walls[i].x > 0){
      Walls[i].x -= 1; // move to left
    }else{
			// score a point if pipe not invisible
			if(Walls[i].invisible == 0){
				Score();
			}
      Walls[i].x = SCREENW ;
      // new gap location
      Walls[i].y = NewPipeGap();
      // re-enable, if recently killed bird
      Walls[i].invisible = 0;
    }
  }
}

void Draw(void){
  int i,j;
  Nokia5110_ClearBuffer();
   
  // draw walls first
  for(i=0;i<TOTALPIPES;i++){
    // pipes
		// if not invisible
		if( Walls[i].invisible == 0){
			// from lower pipeend to ground
			for(j=Walls[i].y+PIPEH;j<SCREENH;j=j+PIPEH){
				Nokia5110_PrintBMP(Walls[i].x, j, Walls[i].image[0], 0);
			}
			// from upper pipeend to sky
			for(j=Walls[i].y-PIPEGAPH-PIPEENDH;j>=PIPEH;j=j-PIPEH){
				Nokia5110_PrintBMP(Walls[i].x, j, Walls[i].image[0], 0);
			}
			// lower pipeend
			Nokia5110_PrintBMP(Walls[i].x, Walls[i].y, Walls[i].image[1], 0);
			// upper pipeend
			Nokia5110_PrintBMP(Walls[i].x, Walls[i].y - PIPEGAPH, Walls[i].image[1], 0);
		}
  }
   
  // draw player
  Nokia5110_PrintBMP(Player.x, Player.y, Player.image[Game.frame], 0); // player
   
  // draw life remaining
  for(i=Player.life;i>0;i--){
    Nokia5110_PrintBMP(LIFEX - ((i)*LIFEW), LIFEY, heart, 0);
  }
  Nokia5110_DisplayBuffer();      // draw buffer
}

// **************DAC_Init*********************
// Initialize 4-bit DAC 
void DAC_Init(void){
	volatile unsigned long delay;
 // PB3, 	PB2, 	PB1, 	PB0
 // DAC3, DAC2, DAC1, DAC0
	SYSCTL_RCGC2_R |= 0x02;          // 1) activate Port B
  delay = SYSCTL_RCGC2_R;          // allow time for clock to stabilize
                                   // 2) no need to unlock PB5-0
  GPIO_PORTB_AMSEL_R &= ~0xFF;     // 3) disable analog functionality on PB5-0
  GPIO_PORTB_PCTL_R = 0x00000000;  // 4) configure PB7-0 as GPIO
  GPIO_PORTB_DIR_R |= 0x3F;        // 5) make PB5-0 out
  GPIO_PORTB_AFSEL_R &= ~0x3F;     // 6) disable alt funct on PB5-0
  GPIO_PORTB_DR8R_R |= 0x3F;       // enable 8 mA drive on PB5-0
  GPIO_PORTB_DEN_R |= 0x3F;        // 7) enable digital I/O on PB5-0
}

// **************DAC_Out*********************
// output to DAC
// Input: 4-bit data, 0 to 15 
// Output: none
void DAC_Out(unsigned long data){
  GPIO_PORTB_DATA_R = data & 0x0F;
}


int main(void){
  TExaS_Init(SSI0_Real_Nokia5110_Scope);  // set system clock to 80 MHz
  // Initialize SysTick interrupts to trigger at 30 Hz, 33.33 ms
  // (1/30)/0.0000000125 = 2666667
  SysTick_Init(2666666);
  Random_Init(NVIC_ST_CURRENT_R);        // seed randomizer
	Sound_Init();
  ADC0_Init();    // initialize ADC0, channel 1, sequencer 3
  Nokia5110_Init();
  Nokia5110_ClearBuffer();                // clear buffer
	// Flappy bird home screen
	Nokia5110_PrintBMP(0, 47, FlappyBird, 0);
  Nokia5110_DisplayBuffer();      // draw buffer
	Delay100ms(30);
  Init();     // initialize game, player, and walls
	InitPlayer();
	InitWalls();
  Draw();     // draw walls and player
  EnableInterrupts();
  while(1){

    // only process game after systick
    if( Flag == 1){
      if(Player.life > 0){
        Move();
        if(DetectCollision()){
					// player hits ground, lose 1 life
					Player.life--;
					// collision bmp
					Game.frame = PLAYERCOLLISIONFRAME;
				}
        Draw();
        //Delay100ms(1);
               
				// advance frame
				Game.frame++;
				// do not display collision frame , it is reserved for Collision
				if(Game.frame == PLAYERCOLLISIONFRAME){
						Game.frame = 0;
				}
				// loop animation
				if(Game.frame > PLAYERFRAMES){
						Game.frame = 0;
				}
				// reset semaphone
				Flag = 0;
           
      }else{
				// end of game
				// check for high score

				if( Game.score > Game.highscore){
						Game.highscore = Game.score;
				}
				Nokia5110_Clear();
				Nokia5110_SetCursor(1, 1);
				Nokia5110_OutString("GAME OVER");

				Nokia5110_SetCursor(2, 2);
				Nokia5110_OutUDec(Game.score);
				Nokia5110_SetCursor(1, 3);
				Nokia5110_OutString("HiScore");
				Nokia5110_SetCursor(2, 4);
				Nokia5110_OutUDec(Game.highscore);
		 
				// reset score
				Game.score = 0;
				// reset frame
				Game.frame = 0;
				// setup player for new game
				InitPlayer();
				InitWalls();
				// pause before automatically restarting
				Delay100ms(30);
		 }
    }
  }
}


// Timer2 for DAC sound
void Timer2_Init(unsigned long period){
  unsigned long volatile delay;
  //SYSCTL_RCGCTIMER_R |= 0x04;   // 0) activate timer2
  //delay = SYSCTL_RCGCTIMER_R;
  // use legacy register to match nokia usage
  SYSCTL_RCGC1_R |= 0x40000;   // Timer2 using the legacy register
  delay = SYSCTL_RCGC1_R;
  TimerCount = 0;
  Semaphore = 0;
  TIMER2_CTL_R = 0x00000000;    // 1) disable timer2A during setup
  TIMER2_CFG_R = 0x00000000;    // 2) configure for 32-bit mode
  TIMER2_TAMR_R = 0x00000002;   // 3) configure for periodic mode, default down-count settings
  TIMER2_TAILR_R = period-1;    // 4) reload value
  TIMER2_TAPR_R = 0;            // 5) bus clock resolution
  TIMER2_ICR_R = 0x00000001;    // 6) clear timer2A timeout flag
  TIMER2_IMR_R = 0x00000001;    // 7) arm timeout interrupt
  NVIC_PRI5_R = (NVIC_PRI5_R&0x00FFFFFF)|0x80000000; // 8) priority 4
  // interrupts enabled in the main program after all devices initialized
  // vector number 39, interrupt number 23
  NVIC_EN0_R = 1<<23;           // 9) enable IRQ 23 in NVIC
  TIMER2_CTL_R = 0x00000001;    // 10) enable timer2A
}
void Timer2A_Handler(void){
  TIMER2_ICR_R = 0x00000001;   // acknowledge timer2A timeout
  Semaphore = 1; // trigger
	Play();
}
void Delay100ms(unsigned long count){
    unsigned long volatile time;
  while(count>0){
    time = 727240;  // 0.1sec at 80 MHz
    while(time){
      time--;
    }
    count--;
  }
}


void ADC0_Init(void){  
  volatile unsigned long delay;
  SYSCTL_RCGC2_R |= 0x00000010;   // 1) activate clock for Port E
  delay = SYSCTL_RCGC2_R;         //    allow time for clock to stabilize
  GPIO_PORTE_DIR_R &= ~0x04;      // 2) make PE2 input
  GPIO_PORTE_AFSEL_R |= 0x04;     // 3) enable alternate function on PE2
  GPIO_PORTE_DEN_R &= ~0x04;      // 4) disable digital I/O on PE2
  GPIO_PORTE_AMSEL_R |= 0x04;     // 5) enable analog function on PE2
  SYSCTL_RCGC0_R |= 0x00010000;   // 6) activate ADC0
  delay = SYSCTL_RCGC2_R;      
  SYSCTL_RCGC0_R &= ~0x00000300;  // 7) configure for 125K
  ADC0_SSPRI_R = 0x0123;          // 8) Sequencer 3 is highest priority
  ADC0_ACTSS_R &= ~0x0008;        // 9) disable sample sequencer 3
  ADC0_EMUX_R &= ~0xF000;         // 10) seq3 is software trigger
  ADC0_SSMUX3_R &= ~0x000F;       // 11) clear SS3 field
  ADC0_SSMUX3_R += 1;             //    set channel Ain1 (PE2)
  ADC0_SSCTL3_R = 0x0006;         // 12) no TS0 D0, yes IE0 END0
  ADC0_ACTSS_R |= 0x0008;         // 13) enable sample sequencer 3
  // enable dither to reduce instability in reading
  //ADC0_CTL_R = 0x40;      // set bit 6 to enable dither (p847)
  //ADC0_SAC_R = 0x6;     // set bits 2,1 for max samples in averaging
}


//------------ADC0_In------------
// Busy-wait Analog to digital conversion
// Input: none
// Output: 12-bit result of ADC conversion
unsigned long ADC0_In(void){
  unsigned long result;
  ADC0_PSSI_R = 0x0008;            // 1) initiate SS3
  while((ADC0_RIS_R&0x08)==0){};   // 2) wait for conversion done
  result = ADC0_SSFIFO3_R&0xFFF;   // 3) read result
  ADC0_ISC_R = 0x0008;             // 4) acknowledge completion
  return result;
}


// Initialize SysTick interrupts
void SysTick_Init(unsigned long period){
  NVIC_ST_CTRL_R = 0;         // disable SysTick during setup
  NVIC_ST_RELOAD_R = period-1;// reload value
  NVIC_ST_CURRENT_R = 0;      // any write to current clears it
  NVIC_SYS_PRI3_R = (NVIC_SYS_PRI3_R&0x00FFFFFF)|0x40000000; // priority 2        
  NVIC_ST_CTRL_R = 0x07; // enable SysTick with core clock and interrupts
    Flag = 0;

}
